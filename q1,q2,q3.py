import numpy as np
import pandas as pd
df = pd.read_csv("german.data-numeric", sep='\s+', header=None)
print(df.shape)
y=df.iloc[:,-1]
x=df.iloc[:,:-1]
# question 1
# function for entropy calculation
def entropy(colu):
  [values,counts]=np.unique(colu,return_counts=True)
  ent=0
  prob=counts/len(colu)
  for p in prob:
    ent-=p*np.log2(p)
  return float(ent)

h_s=entropy(y)
print(h_s)
entr_all_cols=[0  for i in range(x.shape[1])]
for i in range(x.shape[1]):
  entr_all_cols[i]=entropy(x.iloc[:,i])
print(entr_all_cols)

