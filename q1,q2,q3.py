def weights(df,feautur):
  [values,counts]=np.unique(df.iloc[:,feautur],return_counts=True)
  weigh_enr=0
  for value,c in zip(values,counts):
    sub_dataset=df[df.iloc[:,feautur]==value]
    entr=entropy(sub_dataset.iloc[:,-1])
    weigh_enr+=(c/len(df))*entr
  return weigh_enr
weigh_entopy=[]
for i in range(x.shape[1]):
  weigh_entopy.append(weights(df,i))
minvalue=h_s-weigh_entopy[0]
min_id=0
for i in range(1,x.shape[1]):
  ig=h_s-weigh_entopy[i]
  if(ig>minvalue):
    minvalue=ig
    min_id=i
print("column",min_id," can be selected as root node")